# Function need to takes as input a chess booard. Iterate trough 
# the board and check if piece is black/white after, that add to 
# position value depending on the piece position.

"""
P = 100
N = 320
B = 330
R = 500
Q = 900
K = 2000
"""

def evaluate(board):
  position = 0

# Iterate trough board
  for square in algebraic_notation:
    piece = board.piece_at(chess.parse_square(f"{square}"))

# Check if there is a piece in the square
    if hasattr(piece, "piece_type"):
      print(position)
  
# White piece
      if piece.symbol().islower():
        
# Pawn
        if piece.symbol() == "p":
          position += 100
          
# Knight
        elif piece.symbol() == "n":
          position += 320

# Bishop
        elif piece.symbol() == "b":
          position += 330

# Rook
        elif piece.symbol() == "r":
          position += 500

# Queen
        elif piece.symbol() == "q":
          position += 900

# KIng
        elif piece.symbol() == "k":
          position += 2000 

# Piece is black
      elif piece.symbol().isupper():

# Pawn
        if piece.symbol() == "P":
          position -= 100
          
# Knight
        elif piece.symbol() == "N":
          position -= 320

# Bishop
        elif piece.symbol() == "B":
          position -= 330

# Rook
        elif piece.symbol() == "R":
          position -= 500

# Queen
        elif piece.symbol() == "Q":
          position -= 900

# KIng
        elif piece.symbol() == "K":
          position -= 2000 

  return position

print(evaluate(board))



